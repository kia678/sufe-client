//! # xboard-core
//!
//! Shared business core for `xboard-client`. Holds the single implementation
//! of every non-UI concern:
//!
//! - [`api`]      ‚Äî typed Xboard HTTP client (V1 + V2 surfaces)
//! - [`profile`]  ‚Äî subscription text fetcher with ETag caching
//! - [`kernel`]   ‚Äî [`kernel::KernelDriver`] trait + mihomo / xray drivers
//! - [`tunnel`]   ‚Äî platform tunnel adapters (system proxy, VpnService, TUN)
//! - [`updater`]  ‚Äî signed kernel + app self-update pipeline
//! - [`storage`]  ‚Äî SQLite + secure key storage abstraction
//! - [`ipc`]      ‚Äî UI ‚áÑ kernel transport (HTTP for mihomo, sockets for VPN)
//!
//! UI shells (Tauri, Android Compose) consume this crate through `Arc`-shared
//! handles; no business logic lives in either UI codebase.

