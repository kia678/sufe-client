/// Crate version (matches `Cargo.toml`).
///
/// Returns `String` (not `&'static str`) because the UniFFI `string` type
/// in `ffi.udl` lowers to an owned `String` on the FFI boundary ‚Äî the
/// scaffolding macro can't borrow into the static segment of the cdylib.
pub fn version() -> String {
    env!("CARGO_PKG_VERSION").to_string()
}

/// FFI-facing wrapper around [`profile::patch_singbox`]. Translates the
/// public `TunnelMode` enum to the internal one and re-raises errors as
/// `FfiError`. Lives at the crate root because UniFFI's `namespace`
/// section resolves free functions from there.
pub fn render_singbox_config(
    subscribe_yaml: String,
    external_controller: String,
    secret: String,
    mixed_port: u16,
    mode: ffi::TunnelMode,
) -> Result<String, ffi::FfiError> {
    let internal = match mode {
        ffi::TunnelMode::Tun => profile::TunnelMode::Tun,
        ffi::TunnelMode::SystemProxy => profile::TunnelMode::SystemProxy,
    };
    profile::patch_singbox(
        &subscribe_yaml,
        &external_controller,
        &secret,
        mixed_port,
        internal,
    )
    .map_err(ffi::FfiError::from)
}
